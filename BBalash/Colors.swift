import Foundation
import UIKit

class Colors{


    func Blue() -> UIColor{
        return  UIColor.init(red: 32/255, green: 141/255, blue: 1, alpha: 1)
    }
    func Yellow() -> UIColor{
        return  UIColor.init(red: 248/255, green: 186/255, blue: 0, alpha: 1)
    }
}
